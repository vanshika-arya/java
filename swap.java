public class swap {
    public static void main(String[] args) {
        int a=5;
        int b=3;
        System.out.println(a);
        System.out.println(b);
        int temp=a;
        a=b;
        b=temp;
        System.out.println(a +" "+ b);
        int p=14;
        int q=17;
        p=p+q;
        q=p-q;
        p=p-q;
        System.out.println(p +" "+ q);
        
        

    }
    
}
